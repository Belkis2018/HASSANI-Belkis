		 <h3> Comment traiter?</h3>
		 <p>La prescription du médecin La pharyngite-amygdalite d’origine virale ne nécessite aucun traitement médical particulier. 
		 <br> Par contre, l’infection à streptocoque doit être traitée par des antibiotiques.Un traitement à base d’antibiotiques permet de réduire la durée de la maladie ainsi que de prévenir les complications et la propagation de l’infection. Il est très important de poursuivre le traitement pendant toute la durée prescrite, même si les symptômes ont disparu. Interrompre le traitement peut entraîner une rechute, causer des complications et favoriser l’apparition de bactéries résistantes aux antibiotiques.
         <br>Pour éviter une réinfection, jetez la brosse à dents de votre enfant et remplacez-la par une nouvelle 24 heures après le début du traitement antibiotique.
         <br>En cas d’amygdalites à répétition ou de complications médicales, le médecin pourrait envisager une chirurgie pour enlever les amygdales.</p>	  
