		  </ul>
		  <h3> Soins et conseils pratiques</h3>
		  <ul>
		  <li>Favorisez les activités calmes.<br> Il n’est pas nécessaire que votre enfant garde le lit toute la journée, mais il doit se reposer. </li>
		  <li>Donnez-lui souvent à boire.</li>
		  <li>Pour soulager sa fièvre, donnez-lui de l’acétaminophène u de l’ibuprofène <br> en vous conformant aux indications et en respectant les doses recommandées en fonction de son poids. <br>
