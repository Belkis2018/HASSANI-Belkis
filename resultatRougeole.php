         <br> comme l’Aspirine®, à un enfant ni à un adolescent.</li>
		  <li>Surveillez les signes de complications :<br> maux d’oreilles, de gorge ou de tête, vomissements, diarrhée, troubles respiratoires ou convulsions. </li>
