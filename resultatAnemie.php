	     <h1 align="center" >L'anémie</h1>
		 <h3> Comment traiter?</h3>
	     <p>  Pour déterminer si votre enfant souffre d’anémie, une prise de sang est nécessaire. Ce bilan sanguin vise à mesurer la quantité d’hémoglobine dans son sang et à trouver les causes de l’anémie en évaluant, par exemple, ses réserves de fer. 
		    Si le médecin croit que l’anémie est causée par une infection ou une autre maladie, il peut prescrire d’autres examens à votre enfant.
			Lorsque l’anémie est confirmée, il est important de la traiter. Sinon, les effets peuvent être permanents. 
			Si l’anémie est causée par un manque d’apport en fer, le personnel médical vous conseillera quant à l’alimentation à donner à votre enfant. 
			Votre tout-petit devra aussi prendre des suppléments de fer pendant quelques mois. 
			Dans le cas d’une anémie causée par une infection ou une maladie chronique, le traitement peut être différent.  </p>
		  <h3> Comment prévenir?</h3>
            <p> L’anémie causée par un manque d’apport en fer peut être prévenue par une alimentation variée.
            Dès la naissanc , le lait maternel ou les préparations commerciales enrichies en fer pour nourrissons aident l’enfant à combler ses besoins. 
			À partir de 6 mois, introduisez des aliments complémentaires riches en fer. 
			En plus des céréales pour bébés enrichies en fer, Santé Canada recommande de donner à votre bébé des viandes maigres, du poisson, des oeufs, des légumineuses et du tofu.
			Par la suite, offrez à votre enfant des légumes comme les épinards, le brocoli, les choux de Bruxelles, les petits pois et les haricots. Donnez-lui aussi des aliments riches en vitamine C 
			(ex. : oranges, tomates et poivrons), car ils aideront son organisme à absorber le fer.  </p>			
	   
